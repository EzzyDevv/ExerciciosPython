num = [2, 5, 9, 1]

# Tamanho da lista
print(len(num))

# Adicionando um valor em um índice específico
num[3] = 4
print(num)

# Adicionado um valor no final da lista
num.append(7)
print(num)

# Mostrando a fila ordenadamente
print(sorted(num))

# Inserindo um valor em um índice específico da lista;
num.insert(1, 999)
print(num)

# Removendo um valor da lista
num.remove(5)
print(num)